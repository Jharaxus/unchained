let aaveAddress = "0x7Fc66500c84A76Ad7e9c93437bFc5Ac33E2DDaE9";
let vaultAddress = "0x47c05bcca7d57c87083eb4e586007530ee4539e9";

const web3 = new Web3(Web3.givenProvider);

let aaveContract = new web3.eth.Contract(erc20ABI, aaveAddress);

let activeAccount = null;
let currentTokenPrice = 0;

async function getAaveBalance() {
  let balance = await aaveContract.methods.balanceOf(activeAccount).call();
  const balanceConverted = balance / 10**18;
  return balanceConverted;
}

let walletBalance = document.getElementById("wallet-deposit-token-balance");

window.addEventListener('addressToCatch', function(event) {
  activeAccount = event.detail;
  console.log(activeAccount);
  updateAaveBalance()
});

function updateAaveBalance() {
  if (activeAccount != null) {
    getAaveBalance().then(function (result) {
      walletBalance.textContent = result.toFixed(2)
      const walletBalanceWorthSend = new CustomEvent('UpdateDeposit', {detail: result});
      window.dispatchEvent(walletBalanceWorthSend);
    });
  }
}

window.addEventListener('switchedToAave', function(event) {
  updateAaveBalance()
})

window.addEventListener('tokenDataFetchEvent', function(event) {
  currentTokenPrice = event.detail;
});

let amountToDeposit = 0;

async function approveAsset() {
  const amountRequested = BigInt(document.getElementById("wished-amount-deposit").value);
  document.getElementById("wished-amount-deposit").value = ""
  const convertionRate = BigInt(Math.floor(currentTokenPrice * 1000))
  amountToDeposit = 10n**21n * amountRequested / convertionRate;
  console.log("Will approve " + amountToDeposit);

  const provider = new ethers.providers.Web3Provider(window.ethereum)
  const signer = provider.getSigner();

  const aaveContract = new ethers.Contract(aaveAddress, erc20ABI, signer)


  let txApproval = await aaveContract.approve(vaultAddress, amountToDeposit)
  let resApproval = await txApproval.wait()
  

  console.log("Approve done !")
  console.log(resApproval);
}

async function depositAsset() {
  console.log("Will deposit " + amountToDeposit);

  const provider = new ethers.providers.Web3Provider(window.ethereum)
  const signer = provider.getSigner();

  const alphaVaultContract = new ethers.Contract(vaultAddress, erc4626ABI, signer)

  let txDeposit = await alphaVaultContract.deposit(amountToDeposit, activeAccount);
  let resDeposit = await txDeposit.wait()

  console.log("Deposit done !")
  console.log(resDeposit);
}

async function withdrawAssets() {
  const amountRequested = BigInt(document.getElementById("wished-amount-withdraw").value);
  document.getElementById("wished-amount-withdraw").value = ""
  const convertionRate = BigInt(Math.floor(currentTokenPrice * 1000))
  let amountToWithdraw = 10n**21n * amountRequested / convertionRate;
  console.log("Will withdraw " + amountToWithdraw);

  const provider = new ethers.providers.Web3Provider(window.ethereum)
  const signer = provider.getSigner();

  const alphaVaultContract = new ethers.Contract(vaultAddress, erc4626ABI, signer)

  console.log("waiting-withdraw")

  let txWithdraw = await alphaVaultContract.withdraw(amountToWithdraw, activeAccount, activeAccount);
  let resWithdraw = await txWithdraw.wait()

  console.log("Withdraw done !")
  console.log(resWithdraw);
}

